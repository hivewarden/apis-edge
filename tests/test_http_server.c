// Token read from the file generated by http_server_init()
static char g_auth_token[64] = {0};

/**
 * Read the local auth token from the file created by init_local_auth_token().
 * Tries both dev and production paths.
 */
static int read_auth_token(void) {
    const char *paths[] = {
        "./data/apis/local_auth_token",
        "../data/apis/local_auth_token",
        "/data/apis/local_auth_token",
        NULL
    };

    for (int i = 0; paths[i]; i++) {
        FILE *fp = fopen(paths[i], "r");
        if (fp) {
            size_t n = fread(g_auth_token, 1, sizeof(g_auth_token) - 1, fp);
            fclose(fp);
            if (n > 0) {
                g_auth_token[n] = '\0';
                return 0;
            }
        }
    }

    fprintf(stderr, "WARNING: Could not read auth token file\n");
    return -1;
}

