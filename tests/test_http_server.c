typedef struct {
    int status_code;
    char body[8192];
    size_t body_len;
    char content_type[128];
} http_response_t;

/**
 * Send an HTTP request. If auth_token is non-NULL, adds Authorization header.
 */
static int http_request(uint16_t port, const char *method, const char *path,
                        const char *body, const char *auth_token,
                        http_response_t *response) {
    // Create socket
    int sock = socket(AF_INET, SOCK_STREAM, 0);
    if (sock < 0) {
        perror("socket");
        return -1;
    }

    // Connect
    struct sockaddr_in addr = {
        .sin_family = AF_INET,
        .sin_port = htons(port),
    };
    inet_pton(AF_INET, "127.0.0.1", &addr.sin_addr);

    if (connect(sock, (struct sockaddr *)&addr, sizeof(addr)) < 0) {
        perror("connect");
        close(sock);
        return -1;
    }

    // Build auth header line (empty string if no token)
    char auth_header[128] = "";
    if (auth_token && strlen(auth_token) > 0) {
        snprintf(auth_header, sizeof(auth_header),
                 "Authorization: Bearer %s\r\n", auth_token);
    }

    // Build request
    char request[2048];
    int req_len;
    if (body && strlen(body) > 0) {
        req_len = snprintf(request, sizeof(request),
            "%s %s HTTP/1.1\r\n"
            "Host: localhost\r\n"
            "%s"
            "Content-Type: application/json\r\n"
            "Content-Length: %zu\r\n"
            "Connection: close\r\n"
            "\r\n"
            "%s",
            method, path, auth_header, strlen(body), body);
    } else {
        req_len = snprintf(request, sizeof(request),
            "%s %s HTTP/1.1\r\n"
            "Host: localhost\r\n"
            "%s"
            "Connection: close\r\n"
            "\r\n",
            method, path, auth_header);
    }

    // Send request
    if (send(sock, request, req_len, 0) < 0) {
        perror("send");
        close(sock);
        return -1;
    }

    // Receive response until the server closes the connection.
    char buffer[8192];
    size_t total_received = 0;
    while (total_received < sizeof(buffer) - 1) {
        ssize_t received = recv(sock, buffer + total_received,
                                sizeof(buffer) - 1 - total_received, 0);
        if (received < 0) {
            perror("recv");
            close(sock);
            return -1;
        }
        if (received == 0) {
            break;
        }
        total_received += (size_t)received;
    }
    close(sock);

    if (total_received == 0) {
        return -1;
    }
    buffer[total_received] = '\0';

    // Parse response
    memset(response, 0, sizeof(*response));

    // Parse status line
    if (sscanf(buffer, "HTTP/1.1 %d", &response->status_code) != 1) {
        return -1;
    }

    // Find Content-Type header
    const char *ct = strstr(buffer, "Content-Type:");
    if (ct) {
        ct += 13;
        while (*ct == ' ') ct++;
        const char *ct_end = strstr(ct, "\r\n");
        if (ct_end) {
            size_t ct_len = ct_end - ct;
            if (ct_len >= sizeof(response->content_type)) {
                ct_len = sizeof(response->content_type) - 1;
            }
            memcpy(response->content_type, ct, ct_len);
            response->content_type[ct_len] = '\0';
        }
    }

    // Find body
    const char *body_start = strstr(buffer, "\r\n\r\n");
    if (body_start) {
        body_start += 4;
        size_t body_len = total_received - (size_t)(body_start - buffer);
        if (body_len >= sizeof(response->body)) {
            body_len = sizeof(response->body) - 1;
        }
        memcpy(response->body, body_start, body_len);
        response->body[body_len] = '\0';
        response->body_len = body_len;
    }

    return 0;
}

