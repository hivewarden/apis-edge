static void test_auth(uint16_t port) {
    printf("\n--- Test: Authentication ---\n");

    http_response_t response;

    // Request without auth token should get 401
    int result = http_request(port, "GET", "/config", NULL, NULL, &response);
    TEST_ASSERT(result == 0, "No-auth request succeeded");
    TEST_ASSERT(response.status_code == 401, "No-auth returns 401");

    // Request with wrong token should get 401
    result = http_request(port, "GET", "/config", NULL, "wrong_token", &response);
    TEST_ASSERT(result == 0, "Wrong-token request succeeded");
    TEST_ASSERT(response.status_code == 401, "Wrong token returns 401");

    // Request with correct token should get 200
    result = http_request(port, "GET", "/config", NULL, g_auth_token, &response);
    TEST_ASSERT(result == 0, "Auth request succeeded");
    TEST_ASSERT(response.status_code == 200, "Correct token returns 200");

    // /status should work without auth (public endpoint)
    result = http_request(port, "GET", "/status", NULL, NULL, &response);
    TEST_ASSERT(result == 0, "Status without auth succeeded");
    TEST_ASSERT(response.status_code == 200, "Status without auth returns 200");
}

